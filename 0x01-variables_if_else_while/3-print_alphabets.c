#include <stdio.h>

/**
  *main- prints the alphabet in lowercase, and then in uppercas.
  *Return: Always 0 (Success)
  */
int main(void)
{
	char c;


	c = 'a';

	c = 'A';
	while
		(c <= 'Z') {
			putchar(c);
			c++;
		}
		while
		 (c <= 'Z') {
			putchar(c);
			c++;
		}
		putchar('\n');

		return (0);

}




